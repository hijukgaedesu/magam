_translations: dict = {}


def set_language(lang: str) -> None:
    """언어를 설정하고 번역 데이터를 교체함"""
    global _translations, _current_lang
    _current_lang = lang.lower()
    
    _translations.clear()
    
    if _current_lang == "en":
        _translations.update(_LANG_EN)
    elif _current_lang == "jp":
        _translations.update(_LANG_JP)
    elif _current_lang == "zh":
        _translations.update(_LANG_ZH)

def t(s: str) -> str:
    """핵심 번역 함수"""
    target = _translations.get(s, s)
    
    if isinstance(target, list):
        return random.choice(target)
    
    return str(target)

def fmt_month(y: int, m: int) -> str:
    import calendar as _cal
    fmt = _translations.get("_cal_month_fmt", "")
    if fmt:
        try:
            return fmt.format(y=y, m=m, m_abbr=_cal.month_abbr[m])
        except: pass
    return f"{y}년 {m}월"

